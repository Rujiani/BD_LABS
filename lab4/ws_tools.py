                    "SELECT u.user_id, "
                    + _sql_inet(ip_stale)
                    + ", NOW() - make_interval(days => "
                    + str(rng.randint(40, 120))
                    + "), "
                    + _sql_str("устаревший safe IP"),
                    "FROM ws_user u WHERE u.login = " + _sql_str(login) + ";",
