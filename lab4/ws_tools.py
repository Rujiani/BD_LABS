def _sql_str(s: str) -> str:
    return "'" + s.replace("'", "''") + "'"


def _sql_text_array(items: Iterable[str]) -> str:
    return "ARRAY[" + ", ".join(_sql_str(x) for x in items) + "]"


def _sql_ident(s: str) -> str:
    return s


def _sql_inet(ip: str) -> str:
    return f"{_sql_str(ip)}::inet"


def _sql_timestamptz(ts: str) -> str:
    return f"TIMESTAMPTZ {_sql_str(ts)}"


def _join_statements(stmts: Iterable[str]) -> str:
    out: list[str] = []
    for s in stmts:
        s = s.strip()
        if not s:
            continue
        if not s.endswith(";"):
            s += ";"
        out.append(s)
        out.append("")
    return "\n".join(out).rstrip() + "\n"


